// scripts/route-pipeline/04-transformRoutes.mjs
// Stage 4: Transform enriched data to GRAPH-compatible JS modules.
//
// Reads Stage 3 enriched JSON and outputs:
//   - enrichedRouteIntel.js  → keyed by destination slug
//   - enrichedPois.js        → flat array of all premium POIs
//
// These modules are imported by buildNetworkGraph.js to hydrate the GRAPH.
// Usage:  node scripts/route-pipeline/04-transformRoutes.mjs

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { destinationsWithCoords } from "./destinations.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ENRICHED_DIR = path.resolve(__dirname, "../../data/route-pipeline/03-enriched");
const TELEMETRY_DIR = path.resolve(__dirname, "../../data/route-pipeline/01-telemetry");
const POI_DIR = path.resolve(__dirname, "../../data/route-pipeline/02-pois");
const OUTPUT_DIR = path.resolve(
  __dirname,
  "../../frontend/rideratlas/src/features/routes/data"
);

function readJsonSafe(filepath) {
  if (!fs.existsSync(filepath)) return null;
  return JSON.parse(fs.readFileSync(filepath, "utf8"));
}

function buildRouteIntelEntry(enriched, telemetry) {
  const telemetryData = telemetry?.telemetry || {};
  return {
    slug: enriched.destination_slug,
    name: enriched.destination_name,
    cinematic_pitch: enriched.cinematic_pitch || null,
    ride_character: enriched.ride_character || null,
    best_season: enriched.best_season || null,
    road_segments: telemetryData.road_segments ?? null,
    passes_found: telemetryData.passes_found ?? null,
    peaks_found: telemetryData.peaks_found ?? null,
    max_altitude_m: telemetryData.max_altitude_m ?? null,
    difficulty_rating: telemetryData.difficulty ?? null,
    terrain_type: telemetryData.terrain ?? null,
    surface_stats: telemetryData.surface_breakdown ?? null,
    enriched_at: enriched.enriched_at || null,
    model: enriched.model || null,
    provider: enriched.provider || null,
  };
}

function slugify(str) {
  return (str || "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

function buildPoiLookup(poiData) {
  const pois = poiData?.pois || [];
  return new Map(pois.map((poi) => [slugify(poi.name), poi]));
}

function buildPoiEntries(enriched, poiData) {
  const poiLookup = buildPoiLookup(poiData);
  const premiumPois = enriched.premium_pois || [];
  return premiumPois.map((poi) => {
    const factualPoi = poiLookup.get(slugify(poi.name));

    return {
      destination_slug: enriched.destination_slug,
      name: poi.name,
      slug: factualPoi?.slug || factualPoi?.global_id || slugify(poi.name),
      global_id: factualPoi?.global_id || null,
      type: (factualPoi?.category || factualPoi?.type) ?? null,
      lat: factualPoi?.lat ?? null,
      lng: factualPoi?.lng ?? null,
      elevation_m: factualPoi?.elevation_m ?? null,
      nearest_airport: factualPoi?.nearest_airport ?? null,
      source: "internal_proprietary_db",
      raw_context: factualPoi?.raw_context ?? null,
      cinematic_description: poi.cinematic_description || null,
      rider_tip: poi.rider_tip || null,
    };
  });
}

function generateJsModule(varName, data) {
  const json = JSON.stringify(data, null, 2);
  return `// Auto-generated by route-pipeline Stage 4.
// Do not edit manually — re-run: node scripts/route-pipeline/04-transformRoutes.mjs
// Generated: ${new Date().toISOString()}

export const ${varName} = ${json};
`;
}

async function main() {
  const destinations = destinationsWithCoords();

  console.log(`[Stage 4] Transforming enriched data → GRAPH-compatible JS modules`);
  console.log(`[Stage 4] Reading: ${ENRICHED_DIR}`);
  console.log(`[Stage 4] Output:  ${OUTPUT_DIR}\n`);

  const routeIntel = {};
  const allPois = [];
  let processed = 0;
  let skipped = 0;

  for (const dest of destinations) {
    const enriched = readJsonSafe(path.join(ENRICHED_DIR, `${dest.slug}.json`));
    if (!enriched) {
      console.log(`  → ${dest.slug}: no enriched data, skipping`);
      skipped++;
      continue;
    }

    const telemetry = readJsonSafe(path.join(TELEMETRY_DIR, `${dest.slug}.json`));
  const poiData = readJsonSafe(path.join(POI_DIR, `${dest.slug}.json`));

    routeIntel[dest.slug] = buildRouteIntelEntry(enriched, telemetry);
  allPois.push(...buildPoiEntries(enriched, poiData));

    console.log(
      `  ✓ ${dest.slug}: intel + ${(enriched.premium_pois || []).length} POIs`
    );
    processed++;
  }

  // Write enrichedRouteIntel.js
  const routeIntelPath = path.join(OUTPUT_DIR, "enrichedRouteIntel.js");
  fs.writeFileSync(routeIntelPath, generateJsModule("ENRICHED_ROUTE_INTEL", routeIntel));
  console.log(`\n  → Wrote ${routeIntelPath}`);
  console.log(`    ${Object.keys(routeIntel).length} destinations`);

  // Write enrichedPois.js
  const poisPath = path.join(OUTPUT_DIR, "enrichedPois.js");
  fs.writeFileSync(poisPath, generateJsModule("ENRICHED_POIS", allPois));
  console.log(`  → Wrote ${poisPath}`);
  console.log(`    ${allPois.length} premium POIs`);

  console.log(`\n[Stage 4] Complete: ${processed} transformed, ${skipped} skipped`);
  console.log(`[Stage 4] Next: Import these in buildNetworkGraph.js to hydrate the GRAPH.`);
}

main().catch((err) => {
  console.error("[Stage 4] Fatal:", err);
  process.exit(1);
});
